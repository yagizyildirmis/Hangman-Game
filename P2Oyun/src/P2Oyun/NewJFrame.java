private boolean sifreKontrol() {
       File sifDosya = new File(SIFRE_DOSYASI);
       String asilSifre = "";

        try {
            if (!sifDosya.exists() || sifDosya.length() == 0) {
                asilSifre = JOptionPane.showInputDialog(this, "İlk defa giriyorsunuz şifre oluşturun: ", "Şifre Belirleme", JOptionPane.INFORMATION_MESSAGE);
                if (asilSifre == null || asilSifre.trim().isEmpty()) {
                    return false;
                }
                BufferedWriter bw = new BufferedWriter(new FileWriter(sifDosya));
                bw.write(asilSifre);
                bw.close();
                
                logtut("Yeni şifren belirlendi.");
            } else {
                BufferedReader br = new BufferedReader(new FileReader(sifDosya));
                asilSifre = br.readLine();
                br.close();
            }
            for (int i = 0; i < 3; i++) {
                String gSifre = JOptionPane.showInputDialog(this, "Şifrenizi girin:  (Kalan Hak: " + (3 - i) + "):", "Giriş", JOptionPane.QUESTION_MESSAGE);
                
                if (gSifre == null) {
                    logtut("Kullanıcı girişi iptal etti.");
                    return false; 
                }
                
                if (gSifre.equals(asilSifre)) {
                    logtut("Başarılı giriş yapıldı.");
                    JOptionPane.showMessageDialog(this, "Giriş Başarılı! Hoş Geldiniz.");
                    return true;
                } else {
                    logtut("Hatalı şifre denemesi.");
                    JOptionPane.showMessageDialog(this, "Şifren hatalı!", "Hata", JOptionPane.ERROR_MESSAGE);
                }
            }
            logtut("3 defa hatalı giriş yapıldı. Program sonlanıyor.");
            JOptionPane.showMessageDialog(this, "3 kez hatalı girdiniz.Sistem kapatılıyor.", "Hata", JOptionPane.ERROR_MESSAGE);
            return false;
            
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Dosya okuma hatası!", "Hata", JOptionPane.ERROR_MESSAGE);
            return false;
        }
    }

    private void logtut(String islem) {
        try {
            BufferedWriter bw = new BufferedWriter(new FileWriter(LOG_DOSYASI, true));
            DateTimeFormatter dtf = DateTimeFormatter.ofPattern("dd/MM/yyyy HH:mm:ss");
            LocalDateTime simdi = LocalDateTime.now();
            
            bw.write("[" + dtf.format(simdi) + "] - " + islem + "\n");
            bw.close();
        } catch (IOException e) {
            e.printStackTrace();
        }
    }

   private String kelimeSec() {
        List<String> list = new ArrayList<>();
        try {
            BufferedReader br = new BufferedReader(new FileReader(KELIMELER_DOSYASI));
            String satir;
        
            while ((satir = br.readLine()) != null) {
                if (satir.trim().length() >= 6) { 
                    list.add(satir.trim().toUpperCase());
                }
            }
            br.close();
            
            Random r = new Random();  
            return list.get(r.nextInt(list.size()));
            
        } catch (Exception e) {
            return "PROGRAMLAMA";
        }
    }

    private void basla() {
        hata = 0; 
        sn = 0; 
        lblSure.setText("Süre: 0 Saniye"); 
        kelime = kelimeSec();
        
        kelimePaneli.removeAll();
        kelimePaneli.setLayout(new FlowLayout());
        
        for (int i = 0; i < kelime.length(); i++) {
            JLabel yildiz = new JLabel("*");
            kelimePaneli.add(yildiz);
        }
       
        kelimePaneli.revalidate();
        kelimePaneli.repaint();
        
        resimDegis(1);
        
        if (timer != null) {
            timer.stop();
        }
        timer = new Timer(1000, e -> {
            sn++;
            lblSure.setText("Süre: " + sn + " Saniye");
        });
        timer.start();
        
        txtHarf.setEnabled(true);
        txtKelime.setEnabled(true);
        btnHarfTahmin.setEnabled(true);
        btnKelimeTahmin.setEnabled(true);
        txtHarf.setText("");
        txtKelime.setText("");
        
        logtut("Yeni oyun başlatıldı.");
    }

