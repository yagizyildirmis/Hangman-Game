   private void harfDene(String t) {
        if (t == null || t.trim().isEmpty()) return;
        
        t = t.toUpperCase();
        boolean varmi = false;
        
        if (t.length() == 1) {
            for (int i = 0; i < kelime.length(); i++) {
                if (String.valueOf(kelime.charAt(i)).equals(t)) {
                    JLabel lbl = (JLabel) kelimePaneli.getComponent(i);
                    lbl.setText(t);
                    varmi = true;
                }
            }
        } 
        else {
            if (kelime.equals(t)) {
                for (int i = 0; i < kelime.length(); i++) {
                    JLabel lbl = (JLabel) kelimePaneli.getComponent(i);
                    lbl.setText(String.valueOf(kelime.charAt(i)));
                }
                varmi = true;
            }
        }
        
        if (varmi) {
            bittiMi(); 
        } else {
            hata++;
            resimDegis(hata + 1);
            
            if (hata >= 10) { 
                oyunBitti(false); 
            }
        }
    }
    private void bittiMi() {
        boolean yildiz = false;
        for (Component c : kelimePaneli.getComponents()) {
            JLabel lbl = (JLabel) c;
            if (lbl.getText().equals("*")) {
                yildiz = true; 
                break;
            }
        }
        
        if (!yildiz) {
            oyunBitti(true);
        }
    }

